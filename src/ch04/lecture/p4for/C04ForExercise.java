package ch04.lecture.p4for;

public class C04ForExercise {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++)
            System.out.println("i = " + i);
        for (int i = 1; i < 6; i++)
            System.out.println("i = " + i);
        for (int i = 0; i < 9; i += 2)
            System.out.println("i = " + i);
        for (int i = 2; i < 11; i += 2)
            System.out.println("i = " + i);
        for (int i = 4; i >= 0; i--)
            System.out.println("i = " + i);
        for (int i = 5; i > 0; i--)
            System.out.println("i = " + i);


    }
}
